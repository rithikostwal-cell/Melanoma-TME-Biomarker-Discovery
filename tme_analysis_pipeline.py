import torch
import torch.nn as nn
import torch.optim as optim
from torch.utils.data import DataLoader, TensorDataset

# 1. Prepare Data for PyTorch
# We use the scaled data from your adata object
input_dim = X_train.shape[1] # This should be 2000
train_tensor = torch.FloatTensor(X_train.values)
test_tensor = torch.FloatTensor(X_test.values)

train_loader = DataLoader(TensorDataset(train_tensor), batch_size=64, shuffle=True)

# 2. Define the Autoencoder Architecture
class Autoencoder(nn.Module):
    def __init__(self, input_dim):
        super(Autoencoder, self).__init__()
        # Encoder: 2000 -> 512 -> 128 -> 32 (Latent Representation)
        self.encoder = nn.Sequential(
            nn.Linear(input_dim, 512),
            nn.ReLU(),
            nn.Linear(512, 128),
            nn.ReLU(),
            nn.Linear(128, 32)
        )
        # Decoder: 32 -> 128 -> 512 -> 2000
        self.decoder = nn.Sequential(
            nn.Linear(32, 128),
            nn.ReLU(),
            nn.Linear(128, 512),
            nn.ReLU(),
            nn.Linear(512, input_dim),
            nn.Sigmoid() # Keeps output in the same normalized range
        )

    def forward(self, x):
        encoded = self.encoder(x)
        decoded = self.decoder(encoded)
        return encoded, decoded

model_ae = Autoencoder(input_dim)
criterion = nn.MSELoss()
optimizer = optim.Adam(model_ae.parameters(), lr=0.001)

epochs = 50
for epoch in range(epochs):
    for data in train_loader:
        img = data[0]
        # Forward
        encoded, decoded = model_ae(img)
        loss = criterion(decoded, img)
        # Backward
        optimizer.zero_grad()
        loss.backward()
        optimizer.step()

    if (epoch+1) % 10 == 0:
        print(f'Epoch [{epoch+1}/{epochs}], Loss: {loss.item():.4f}')

# 1. Extract weights of the first layer (Input -> 512 nodes)
# We take the absolute values because both high positive and high negative weights indicate importance
weights = model_ae.encoder[0].weight.abs().detach().numpy()

# 2. Calculate the mean importance for each of the 2,000 genes across the hidden layer
